static int
hfsfold(int c)
{
	int i;

	i = hfslcase[(c>>8)&255];
	if(i == 0)
		return c;
	return hfslcase[i+(c&255)];
}

static int
hfsinamecmp(Name *a, Name *b)
{
	int an, bn, ac, bc, diff;
	Rune *ap, *bp;

	an = a->len;
	ap = a->name;
	bn = b->len;
	bp = b->name;
	for(;;){
		ac = 0;
		while(an && !ac){
			ac = hfsfold(*ap);
			ap++;
			an--;
		}
		bc = 0;
		while(bn && !bc){
			bc = hfsfold(*bp);
			bp++;
			bn--;
		}
		diff = ac - bc;
		if(diff)
			return diff;
		if(ac == 0)
			return 0;
	}
}

static int
hfsnamecmp(Name *a, Name *b)
{
	int n, diff;
	Rune *ap, *bp;

	n = a->len;
	if(n > b->len)
		n = b->len;
	ap = a->name;
	bp = b->name;
	diff = 0;
	for(; n>0; n--, ap++, bp++){
		diff = *ap - *bp;
		if(diff)
			break;
	}
	if(diff == 0)
		diff = a->len - b->len;
	return diff;
}
